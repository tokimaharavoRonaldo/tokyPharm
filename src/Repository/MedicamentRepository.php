    public function search_medicament($term){
        $qb = $this->createQueryBuilder('medicament');
        $qb->where('medicament.cible LIKE :term');
        $qb ->setParameter('term','%'.$term.'%');
        $qb->orWhere('medicament.name LIKE :term2');
